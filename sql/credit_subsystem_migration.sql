CREATE INDEX IF NOT EXISTS idx_credit_accounts_tenant
ON public.customer_credit_accounts(telegram_id);

CREATE INDEX IF NOT EXISTS idx_credit_ledger_account
ON public.credit_transaction_ledger(account_id);

CREATE OR REPLACE FUNCTION public.increment_debt(account_id_input integer, amount_input numeric)
RETURNS void
LANGUAGE plpgsql
AS $$
BEGIN
    UPDATE public.customer_credit_accounts
    SET outstanding_balance = outstanding_balance + amount_input
    WHERE id = account_id_input;
END;
$$;
