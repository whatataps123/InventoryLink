-- InventoryLink Credit (Utang) subsystem migration
-- Safe to run multiple times.

ALTER TABLE public.stores
ADD COLUMN IF NOT EXISTS debt_grace_period_days integer DEFAULT 30;

CREATE TABLE IF NOT EXISTS public.customer_credit_accounts (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    telegram_id bigint NOT NULL,
    customer_name text NOT NULL,
    contact_info text DEFAULT 'Not Provided'::text,
    outstanding_balance numeric DEFAULT 0.00 CHECK (outstanding_balance >= 0::numeric),
    credit_limit numeric DEFAULT 2000.00,
    oldest_unpaid_credit_date timestamp with time zone,
    last_payment_date timestamp with time zone,
    created_at timestamp with time zone DEFAULT now()
);

ALTER TABLE public.customer_credit_accounts
ADD COLUMN IF NOT EXISTS telegram_id bigint,
ADD COLUMN IF NOT EXISTS customer_name text,
ADD COLUMN IF NOT EXISTS contact_info text DEFAULT 'Not Provided'::text,
ADD COLUMN IF NOT EXISTS outstanding_balance numeric DEFAULT 0.00,
ADD COLUMN IF NOT EXISTS credit_limit numeric DEFAULT 2000.00,
ADD COLUMN IF NOT EXISTS oldest_unpaid_credit_date timestamp with time zone,
ADD COLUMN IF NOT EXISTS last_payment_date timestamp with time zone,
ADD COLUMN IF NOT EXISTS created_at timestamp with time zone DEFAULT now();

CREATE TABLE IF NOT EXISTS public.credit_transaction_ledger (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    account_id integer NOT NULL,
    transaction_type text NOT NULL,
    amount numeric NOT NULL CHECK (amount > 0::numeric),
    reference_notes text,
    logged_at timestamp with time zone DEFAULT now()
);

ALTER TABLE public.credit_transaction_ledger
ADD COLUMN IF NOT EXISTS account_id integer,
ADD COLUMN IF NOT EXISTS transaction_type text,
ADD COLUMN IF NOT EXISTS amount numeric,
ADD COLUMN IF NOT EXISTS reference_notes text,
ADD COLUMN IF NOT EXISTS logged_at timestamp with time zone DEFAULT now();

